def focal_loss(gamma=2., alpha=0.25):

    def loss(y_true, y_pred):

        epsilon = 1e-7
        y_pred = K.clip(y_pred, epsilon, 1 - epsilon)

        pt = tf.where(K.equal(y_true, 1), y_pred, 1 - y_pred)

        return -K.mean(alpha * K.pow(1 - pt, gamma) * K.log(pt))

    return loss


