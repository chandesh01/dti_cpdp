    cnn1 = Conv1D(64, 3, padding='same', activation='relu')(inputs)
    cnn2 = Conv1D(64, 5, padding='same', activation='relu')(inputs)
    cnn3 = Conv1D(64, 7, padding='same', activation='relu')(inputs)

    cnn = Concatenate()([cnn1, cnn2, cnn3])  # 192 filters
    cnn = MaxPooling1D(2)(cnn)
    cnn = Dropout(0.3)(cnn)

    # Proper Residual connection
    res = Conv1D(192, 1, padding='same')(inputs)
    res = MaxPooling1D(2)(res)
    cnn = Add()([cnn, res])

    # BiLSTM
    lstm = Bidirectional(LSTM(64, return_sequences=True))(cnn)
    lstm = Dropout(0.3)(lstm)

    # Self Attention
    attention = MultiHeadAttention(num_heads=4, key_dim=32)(lstm, lstm)
    attention = Add()([attention, lstm])
    attention = LayerNormalization(epsilon=1e-6)(attention)

    # Global pooling
    x = GlobalAveragePooling1D()(attention)
    x = Dense(128, activation='relu')(x)
    x = Dropout(0.4)(x)

    outputs = Dense(1, activation='sigmoid')(x)

    model = Model(inputs, outputs)

    model.compile(
        optimizer=tf.keras.optimizers.Adam(learning_rate=0.0005),
        loss=focal_loss(),
        metrics=['accuracy']
    )

    return model


